public class Usuario {

    private String _id;
    private String _password;


    public Usuario() {

    }


    public void setID(String id) {
        this._id = id;
    }

    public String getID() {
        return this._id;
    }

    public void set_password(String _password) {
        this._password = _password;
    }

    public String get_password() {
        return this._password;
    }


}